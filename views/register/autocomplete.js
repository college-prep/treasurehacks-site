function toSearchable (str) {
  return str.replace(/\. |[^a-zA-Z&@ ]|^The\s+/gi, '')
    .replace(/@/g, 'at')
}

fetch('https://raw.githubusercontent.com/MLH/mlh-policies/main/schools.csv')
  .then(x => x.text())
  .then(result => {
    const schools = result.split('\n').slice(1)
      .map(line => { try { return JSON.parse(line) } catch { return line } })
      .map(line => line.trim().replace(/^The\s+/i, ''))
      .sort()
    schoolList = schools.map(s => ({
      name: s,
      searchText: toSearchable(s)
    }))
    populateSchoolsAutocomplete()
  })
  .catch(e => console.error('Could not load list of schools'))

function showAutocomplete (event) {
  const searchTerm = toSearchable(event.target.value).toLowerCase()
  const container = event.target.parentNode.querySelector('.autofill-container')
  const hasMatch = [...container.children].map(child => {
    const matchTerms = JSON.parse(child.dataset.terms)
    const hasMatch = matchTerms.some(term => term.toLowerCase().includes(searchTerm))

    const keywordMatches = matchTerms[0].toLowerCase().split(' ')
    const keywordCount = searchTerm.trim().split(' ')
      .filter(word => keywordMatches.find(kw => kw.includes(word))).length
    const wordCount = searchTerm.trim().split(' ').length

    const order = hasMatch ? 0 : 1000 - keywordCount
    const hasExact = matchTerms.some(term => term.toLowerCase() === searchTerm)
    const show = !!event.target.value && order < 1000 - wordCount / 2
    child.toggleAttribute('data-show', show) // Keyword matches > 50% of word count
    // If there is an exact match, show it first
    child.style.order = hasExact ? -1 : order

    return show
  }).some(m => !!m)
  container.classList.toggle('empty', !hasMatch)
}

document.querySelectorAll('[data-autofill]').forEach(el => {
  el.addEventListener('input', showAutocomplete)
  el.querySelector('input').addEventListener('focus', e => {
    el.classList.add('hold-focus')
    showAutocomplete(e)
  })
  el.addEventListener('focusout', () => {
    const targetContained = !!el.querySelector('.input-wrapper :hover')
    el.classList.toggle('hold-focus', targetContained)
  }, { capture: true })
})

function addAutocompleteChild (container, child, terms) {
  child.dataset.terms = JSON.stringify(terms)
  container.appendChild(child)
  child.addEventListener('click', () => {
    const input = nodeTree(child, 2).querySelector('input')
    input.value = child.innerText
    nodeTree(child, 2).classList.remove('hold-focus')
    save()
  })
}

